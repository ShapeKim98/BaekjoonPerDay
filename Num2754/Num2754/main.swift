import Foundation
let score = readLine()!
if score == "A+" {
    print(4.3)
} else if score == "A0" {
    print(4.0)
} else if score == "A-" {
    print(3.7)
} else if score == "B+" {
    print(3.3)
} else if score == "B0" {
    print(3.0)
} else if score == "B-" {
    print(2.7)
} else if score == "C+" {
    print(2.3)
} else if score == "C0" {
    print(2.0)
} else if score == "C-" {
    print(1.7)
} else if score == "D+" {
    print(1.3)
} else if score == "D0" {
    print(1.0)
} else if score == "D-" {
    print(0.7)
} else {
    print(0.0)
}
